while True:
    day = eval(input("Enter number of week day 1-7, mon-fri: "))
    
    if(day == 1):
        print("Monday")

    elif(day == 2):
        print("Tuesday")

    elif(day == 3):
        print("Wednesday")

    elif(day == 4):
        print("Thursday")

    elif(day == 5):
        print("Friday")

    elif(day == 6):
        print("Saturday")

    elif(day == 7):
        print("Sunday")

    else:
        print("Number not valid")
